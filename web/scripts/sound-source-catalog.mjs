function commonsSource(fileName, title, author, license) {
  const encodedFileName = fileName.split('/').map((part) => encodeURIComponent(part)).join('/');
  return {
    title: `File:${fileName.replace(/_/g, ' ')}`,
    displayTitle: title,
    provider: 'Wikimedia Commons',
    pageUrl: `https://commons.wikimedia.org/wiki/File:${encodedFileName}`,
    sourceUrl: `https://commons.wikimedia.org/wiki/Special:Redirect/file/${encodedFileName}`,
    author,
    credit: title,
    license,
    usageTerms: license,
    attributionRequired: !/public domain|cc0/i.test(license),
  };
}

function freesoundSource({ id, title, author, license, tags = [], duration, displayTitle }) {
  return {
    title,
    displayTitle: displayTitle ?? title,
    provider: 'Freesound',
    pageUrl: `https://freesound.org/people/${encodeURIComponent(author)}/sounds/${id}/`,
    sourceUrl: `https://cdn.freesound.org/previews/${String(id).slice(0, 3)}/${id}_hq.mp3`,
    freesoundId: id,
    author,
    credit: `${title} by ${author}`,
    license,
    usageTerms: license,
    description: displayTitle ?? title,
    categories: tags.join(' '),
    duration,
    attributionRequired: !/public domain|cc0/i.test(license),
  };
}

function directAudioSource(source) {
  return {
    ...source,
    url: source.sourceUrl,
    mime: source.mime ?? 'audio/ogg',
    size: source.size ?? 0,
    usageTerms: source.license,
    description: source.displayTitle ?? source.title,
    categories: source.tags?.join(' ') ?? '',
    attributionRequired: !/public domain|cc0/i.test(source.license),
  };
}

function archiveSource(source) {
  return {
    ...source,
    archiveMember: source.archiveMember,
    attributionRequired: !/public domain|cc0/i.test(source.license),
    usageTerms: source.license,
    description: source.displayTitle ?? source.title,
    categories: source.tags?.join(' ') ?? '',
  };
}

function actionSource(query, includeKeywords, sourceClass, excludeKeywords = []) {
  return {
    query,
    includeKeywords,
    sourceClass,
    excludeKeywords,
  };
}

